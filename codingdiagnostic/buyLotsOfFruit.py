from __future__ import print_function

fruitPrices = {'apples': 2.00, 'oranges': 1.50, 'pears': 1.75,
               'limes': 0.75, 'strawberries': 1.00}


def buyLotsOfFruit(orderList):

    ''' takes a list of(fruit,pound)tuples and returns the cost of your list. If there is some fruit in
        the list which doesnâ€™t appear in fruitPrices it should print an error message and return None'''
    totalCost = 0.0
    for item , num  in orderList:
        if item in fruitPrices:
            totalCost += num * fruitPrices[item]

        else:
            print('Enter right fruit name and number of it ')
            return  None

    return totalCost
        

# Main Method
if __name__ == '__main__':
    "This code runs when you invoke the script from the command line"
    orderList = [('apples', 2.0), ('pears', 3.0), ('limes', 4.0)]
    print('Cost of', orderList, 'is', buyLotsOfFruit(orderList))
